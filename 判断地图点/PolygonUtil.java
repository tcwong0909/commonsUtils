    /**
     * Description 生成地图不规则多边形
     *
     * @param polygonPoint 边界点集合
     * @return
     * @author tcwong
     * @date 2020/11/29
     */
    private static GeneralPath getGeneralPath(List<Point2D.Double> polygonPoint) {
        GeneralPath generalPath = new GeneralPath();
        Point2D.Double startPoint = polygonPoint.get(0);
        generalPath.moveTo(startPoint.getX(), startPoint.getY());
        polygonPoint.remove(0);
        polygonPoint.forEach(point -> {
            generalPath.lineTo(point.getX(), point.getY());
        });
        generalPath.lineTo(startPoint.getX(), startPoint.getY());
        generalPath.closePath();
        return generalPath;
    }

    /**
     * Description 判断目标点在不在版图内
     *
     * @param longitude 经度
     * @param latitude  纬度
     * @return
     * @author tcwong
     * @date 2020/11/29
     */
    public static Boolean isInPolygon(double longitude, double latitude) {
        Point2D.Double targetPoint = new Point2D.Double(latitude, longitude);
        return generalPathMainland.contains(targetPoint)
                || generalPathTaiWan.contains(targetPoint)
                || generalPathHaiNan.contains(targetPoint)
                || generalPathChongMing.contains(targetPoint);
    }

